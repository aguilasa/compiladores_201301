package br.com.furb.comp.gals;

public class Position {
	private int ini;
	private int fim;
	
	public Position(int ini, int fim) {
		super();
		this.ini = ini;
		this.fim = fim;
	}

	public int getIni() {
		return ini;
	}

	public void setIni(int ini) {
		this.ini = ini;
	}

	public int getFim() {
		return fim;
	}

	public void setFim(int fim) {
		this.fim = fim;
	}
	
}
